void configGPIO(void) {
    RCC->AHB1ENR |= RCC_AHB1ENR_GPIOAEN; // Habilitar reloj para GPIOA
    RCC->AHB1ENR |= RCC_AHB1ENR_GPIOCEN; // Habilitar reloj para GPIOC
    
    // Configurar pines PA0-PA3 como salida (para LEDs)
    GPIOA->MODER |= 0x55; // Modo de salida para PA0-PA3

    // Configurar botones en PC0, PC1, PC2
    GPIOC->MODER &= ~0x3F; // Modo de entrada para PC0, PC1, PC2
    GPIOC->PUPDR |= 0x2A;  // Pull-up en PC0, Pull-down en PC1, externa en PC2

    // Configuración de interrupciones externas
    SYSCFG->EXTICR[0] |= SYSCFG_EXTICR1_EXTI0_PC | SYSCFG_EXTICR1_EXTI1_PC | SYSCFG_EXTICR1_EXTI2_PC;
    EXTI->IMR |= EXTI_IMR_IM0 | EXTI_IMR_IM1 | EXTI_IMR_IM2;
    EXTI->RTSR |= EXTI_RTSR_TR0 | EXTI_RTSR_TR1 | EXTI_RTSR_TR2;

    NVIC_EnableIRQ(EXTI0_IRQn);
    NVIC_EnableIRQ(EXTI1_IRQn);
    NVIC_EnableIRQ(EXTI2_IRQn);
}

void EXTI0_IRQHandler(void) { // Cambiar modo
    if (EXTI->PR & EXTI_PR_PR0) {
        mode ^= 1; // Alternar entre décadas y binario
        EXTI->PR = EXTI_PR_PR0;
    }
}

void EXTI1_IRQHandler(void) { // Incrementar contador
    if (EXTI->PR & EXTI_PR_PR1) {
        counter++;
        EXTI->PR = EXTI_PR_PR1;
    }
}

void EXTI2_IRQHandler(void) { // Decrementar contador
    if (EXTI->PR & EXTI_PR_PR2) {
        counter--;
        EXTI->PR = EXTI_PR_PR2;
    }
}

